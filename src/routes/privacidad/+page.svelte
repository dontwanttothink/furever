<section>
	<h2>Datos que recopilamos</h2>
	<p>En FureverHome, recopilamos los siguientes datos:</p>
	<ul>
		<li>El nombre que ingreses en el formulario de registro.</li>
		<li>Tu dirección de correo electrónico.</li>
		<li>Tu dirección IP en forma anonimizada, conocida como hash.</li>
		<li>
			Cualquier contenido que decidas compartir en la plataforma, como imágenes,
			datos de mascotas o texto.
		</li>
	</ul>
</section>
<section>
	<h2>Uso de los datos</h2>
	<p>
		Usamos tu nombre y dirección de correo electrónico para identificarte y
		asegurarnos de que solo tú puedas acceder a tu cuenta. Los hashes de
		direcciones IP se recopilan por motivos de seguridad, como establecer
		límites de frecuencia de solicitudes para mantener el buen rendimiento de la
		plataforma para todos los usuarios. Finalmente, cualquier contenido que
		publiques se recopila para ponerlo a disposición de otros usuarios.
	</p>
</section>
<section>
	<h2>Cómo compartimos tus datos</h2>
	<p>
		En FureverHome, no compartimos tu información personal con terceros, excepto
		en los siguientes casos:
	</p>
	<ul>
		<li>
			Cuando sea requerido por ley o en respuesta a un proceso legal válido.
		</li>
		<li>
			Cuando utilizamos servicios de terceros, como infraestructura en la nube o
			redes de distribución de contenido (CDN) como Cloudflare, para operar y
			asegurar nuestra plataforma. Estos servicios pueden procesar tus datos
			únicamente para los fines necesarios para proporcionar sus servicios.
		</li>
		<li>
			En caso de una fusión, adquisición o venta de activos, donde tus datos
			podrían transferirse como parte del acuerdo.
		</li>
	</ul>
</section>
<section>
	<h2>Cookies</h2>
	<p>
